/// Marker shapes for point annotations
/// 
/// Defines the visual shape used to mark data points in annotations.
enum MarkerShape {
  /// Circular marker (default)
  circle,

  /// Square marker
  square,

  /// Triangular marker
  triangle,

  /// Diamond-shaped marker
  diamond,

  /// Star-shaped marker
  star,

  /// Cross marker (×)
  cross,

  /// Plus marker (+)
  plus,
}
