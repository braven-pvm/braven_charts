/// Anchor point for text annotations
///
/// Defines how text annotations are positioned relative to their coordinate.
enum AnnotationAnchor {
  /// Anchor at top-left corner
  topLeft,

  /// Anchor at top-center
  topCenter,

  /// Anchor at top-right corner
  topRight,

  /// Anchor at center-left
  centerLeft,

  /// Anchor at center (default)
  center,

  /// Anchor at center-right
  centerRight,

  /// Anchor at bottom-left corner
  bottomLeft,

  /// Anchor at bottom-center
  bottomCenter,

  /// Anchor at bottom-right corner
  bottomRight,
}
